/**
 * 角色数据管理器
 */
export class RoleDataManager {
    /**
     * 获取角色1的配置
     */
    static getRole1Config(index: number): RoleConfig | undefined {
        return Role1Config[index];
    }

    /**
     * 获取角色2的配置
     */
    static getRole2Config(index: number): RoleConfig | undefined {
        return Role2Config[index];
    }

    /**
     * 根据索引名称获取角色1索引
     */
    static getRole1IndexByIndexName(indexName: string): number {
        return Role1Config.findIndex(r => r.indexName === indexName);
    }

    /**
     * 根据 ID 获取角色2索引
     */
    static getRole2IndexById(id: string): number {
        return Role2Config.findIndex(r => r.id === id);
    }

    /**
     * 根据名称获取角色2索引
     */
    static getRole2IndexByName(name: string): number {
        return Role2Config.findIndex(r => r.name === name);
    }

    /**
     * 检查角色1是否被禁止
     */
    static isRole1Banned(index: number): boolean {
        return Role1Config[index]?.banned ?? false;
    }

    /**
     * 检查角色2是否被禁止
     */
    static isRole2Banned(index: number): boolean {
        return Role2Config[index]?.banned ?? false;
    }

    /**
     * 获取合成结果（使用索引，内部转换为稳定的 key）
     * @param role1Index 角色1索引
     * @param role2Index 角色2索引
     * @returns 合成结果，如果不存在或已被禁止则返回null
     */
    static getFusionResult(role1Index: number, role2Index: number): RoleInfo | null {
        // 检查是否被禁止
        if (this.isRole1Banned(role1Index) || this.isRole2Banned(role2Index)) {
            return null;
        }

        // 使用稳定的 key 格式
        const key = getFusionKeyFromIndex(role1Index, role2Index);
        return FusionResultMap.get(key) || null;
    }


    /**
     * 获取所有可用的角色1列表（排除被禁止的）
     */
    static getAvailableRole1List(): RoleConfig[] {
        return Role1Config.filter(r => !r.banned);
    }

    /**
     * 获取所有可用的角色2列表（排除被禁止的）
     */
    static getAvailableRole2List(): RoleConfig[] {
        return Role2Config.filter(r => !r.banned);
    }

    /**
     * 获取角色1的名称（兼容旧代码）
     */
    static getRole1Name(index: number): string {
        return Role1Config[index]?.name || '';
    }

    /**
     * 获取角色2的名称（兼容旧代码）
     */
    static getRole2Name(index: number): string {
        return Role2Config[index]?.name || '';
    }

    /**
     * 根据角色1索引获取所有角色2的合成结果数组
     * 用于兼容 showByRole 方法
     * @param role1Index 角色1索引
     * @returns 数组，索引对应 role2Index，值为合成结果或 null
     */
    static getRole2ResultsArray(role1Index: number): (RoleInfo | null)[] {
        const results: (RoleInfo | null)[] = [];
        for (let role2Index = 0; role2Index < Role2Config.length; role2Index++) {
            // 使用稳定的 key 格式
            const key = getFusionKeyFromIndex(role1Index, role2Index);
            results[role2Index] = FusionResultMap.get(key) || null;
        }
        return results;
    }

    /**
     * 获取所有合成结果（用于图鉴等需要遍历的场景）
     * @returns 合成结果数组，包含 role1Index, role2Index 和 roleInfo
     */
    static getAllFusionResults(): Array<{ role1Index: number; role2Index: number; roleInfo: RoleInfo }> {
        const results: Array<{ role1Index: number; role2Index: number; roleInfo: RoleInfo }> = [];
        FusionResultMap.forEach((roleInfo, key) => {
            // 解析稳定的 key 格式: `${role1IndexName}_${role2Id}`
            const [role1IndexName, role2Id] = key.split('_');
            
            // 从 role1IndexName 找到 role1Index
            const role1Index = Role1Config.findIndex(r => r.indexName === role1IndexName);
            if (role1Index === -1) return; // 跳过无效的 key
            
            // 从 role2Id 找到 role2Index
            const role2Index = Role2Config.findIndex(r => r.id === role2Id);
            if (role2Index === -1) return; // 跳过无效的 key
            
            results.push({ role1Index, role2Index, roleInfo });
        });
        return results;
    }
}

