/**
 * 生成稳定的 FusionResultMap key
 * 使用 role1IndexName 和 role2Id，即使删除配置数据也不会影响
 * @param role1IndexName 角色1的 indexName（如 'al', 'blk'）
 * @param role2Id 角色2的 id（字符串，如 '0', '1'）
 * @returns Map key，格式: `${role1IndexName}_${role2Id}`
 */
function getFusionKey(role1IndexName: string, role2Id: string): string {
    return `${role1IndexName}_${role2Id}`;
}

/**
 * 从索引获取稳定的 key（兼容旧代码）
 * @param role1Index 角色1索引
 * @param role2Index 角色2索引
 * @returns 稳定的 key
 */
function getFusionKeyFromIndex(role1Index: number, role2Index: number): string {
    const role1Config = Role1Config[role1Index];
    const role2Config = Role2Config[role2Index];
    
    if (!role1Config || !role2Config) {
        // Fallback 到旧格式
        return `${role1Index}_${role2Index}`;
    }
    
    const role1IndexName = role1Config.indexName;
    const role2Id = role2Config.id || String(role2Index); // 如果没有 id，使用索引作为 fallback
    
    return getFusionKey(role1IndexName, role2Id);
}

