/**
 * 净分 → 档：低 = 对极占优（I/S/T），高 = 主极占优（E/N/F），中为过渡。
 * 与原先「只累 E/N/F」时在纯极端卷面上结果一致；含 I/S/T 后 C 等选项会改变净分。
 */
export function rgcsNetToTier(net: number): RgcsTier {
    if (net <= -13) return "低";
    if (net >= 13) return "高";
    return "中";
}

/** @deprecated 仅兼容旧「单累计 0–40」思路；请用 rgcsNetToTier */
export function rgcsScoreToTier(score: number): RgcsTier {
    if (score <= 13) return "低";
    if (score <= 26) return "中";
    return "高";
}

export function rgcsComputeCode(answers: number[]): string {
    let E = 0;
    let I = 0;
    let N = 0;
    let S = 0;
    let T = 0;
    let F = 0;
    for (let i = 0; i < 12; i++) {
        const s = RGCS_QUESTIONS[i].scores[answers[i]];
        E += s.e;
        I += s.i;
        N += s.n;
        S += s.s;
        T += s.t;
        F += s.f;
    }
    const t1 = rgcsNetToTier(E - I);
    const t2 = rgcsNetToTier(N - S);
    const t3 = rgcsNetToTier(F - T);
    return `${t1}-${t2}-${t3}`;
}

